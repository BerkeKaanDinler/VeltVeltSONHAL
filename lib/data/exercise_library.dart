VeltExercise? exerciseById(String id) {
  for (final e in kExerciseLibrary) {
    if (e.id == id) return e;
  }
  return null;
}

VeltExercise? exerciseByName(String name) {
  for (final e in kExerciseLibrary) {
    if (e.name.toLowerCase() == name.toLowerCase()) return e;
  }
  return null;
}

List<VeltExercise> filterExercises({
  String muscle = 'All',
  String equipment = 'All',
  String query = '',
}) {
  final q = query.trim().toLowerCase();
  return kExerciseLibrary.where((e) {
    if (muscle != 'All' && e.muscle != muscle) return false;
    if (equipment != 'All' && e.equipment != equipment) return false;
    if (q.isNotEmpty &&
        !e.name.toLowerCase().contains(q) &&
        !e.muscle.toLowerCase().contains(q)) {
      return false;
    }
    return true;
  }).toList();
}
